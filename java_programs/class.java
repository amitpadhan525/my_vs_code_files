class Addition {
    public static void main(String args[]) {
        int x = 10;
        int y = 20;
        int z = x + y;
        int k = x * y;
        int l = x / y;
        int m = x - y;

        System.out.println("The value of addition: " + z);
        System.out.println("The value of multiplication: " + k);
        System.out.println("The value of division: " + l);
        System.out.println("The value of subtraction: " + m);
    }
}
